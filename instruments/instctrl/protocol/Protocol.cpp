#include "Protocol.h"

using namespace Protocols;

/////////////////////////////////////////////////////////////////////
Protocol::Protocol() {

}

/////////////////////////////////////////////////////////////////////
Protocol::~Protocol() {

}

/////////////////////////////////////////////////////////////////////
std::vector<std::string> Protocol::incoming(std::string msg) {
	std::vector<std::string> result;
	result.push_back(msg);
	return result;
}

/////////////////////////////////////////////////////////////////////
std::vector<std::string> Protocol::outgoing(std::string msg) {
	std::vector<std::string> result;
	result.push_back(msg);
	return result;
}

/////////////////////////////////////////////////////////////////////
std::vector<std::string> Protocol::errors() {
	std::vector<std::string> result = _errors;
	_errors.clear();
	return result;
}

/////////////////////////////////////////////////////////////////////
bool Protocol::error() {
	return (_errors.size() > 0);
}
